import java.util.*;

public class conditions {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int num = sc.nextInt();
        if(num % 2 == 0){
            System.out.println("This is Even Number");
        }
        else{
            System.out.println("This is Odd Number");
        }
    }
}
